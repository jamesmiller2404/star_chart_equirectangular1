import fs from 'node:fs/promises';

const inputPath = new URL('../data/constellations/lines_in_20.txt', import.meta.url);
const outputPath = new URL('../src/chart/constellation-boundaries-data.mjs', import.meta.url);

function parseBoundarySegments(source) {
  const segments = [];
  let currentSegment = null;

  for (const line of source.split(/\r?\n/)) {
    if (!line.trim()) continue;

    const match = line.match(/^\s*([0-9.]+)\s+([+-][0-9.]+)\s+(\S+)\s*$/);
    if (!match) throw new Error(`Unable to parse boundary line: ${line}`);

    const [, raText, decText, id] = match;
    if (!currentSegment || currentSegment.id !== id) {
      currentSegment = { id, points: [] };
      segments.push(currentSegment);
    }

    currentSegment.points.push([Number(raText), Number(decText)]);
  }

  return segments.filter((segment) => segment.points.length > 1);
}

function pointBounds(segments) {
  const raValues = [];
  const decValues = [];

  for (const segment of segments) {
    for (const [ra, dec] of segment.points) {
      raValues.push(ra);
      decValues.push(dec);
    }
  }

  return {
    minRa: Math.min(...raValues),
    maxRa: Math.max(...raValues),
    minDec: Math.min(...decValues),
    maxDec: Math.max(...decValues),
  };
}

const source = await fs.readFile(inputPath, 'utf8');
const segments = parseBoundarySegments(source);
const bounds = pointBounds(segments);
const pointCount = segments.reduce((sum, segment) => sum + segment.points.length, 0);

const output = [
  '// Generated by scripts/build-constellation-boundaries.mjs from data/constellations/lines_in_20.txt.',
  'export const CONSTELLATION_BOUNDARIES = ',
  JSON.stringify({
    source: 'data/constellations/lines_in_20.txt',
    sourceDescription: 'Interpolated merged constellation boundary edges in J2000 RA hours and declination degrees.',
    coordinateSystem: 'J2000',
    bounds,
    segmentCount: segments.length,
    pointCount,
    segments,
  }),
  ';',
  '',
].join('\n');

await fs.writeFile(outputPath, output);
console.log(`Wrote ${segments.length} constellation boundary segments and ${pointCount} points to ${outputPath.pathname}`);
