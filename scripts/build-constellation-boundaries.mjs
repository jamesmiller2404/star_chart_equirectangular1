import fs from 'node:fs/promises';

const inputPath = new URL('../app/oldConstellationBoundryScript.py', import.meta.url);
const outputPath = new URL('../src/chart/constellation-boundaries-data.mjs', import.meta.url);
const sourceWidth = 5200;
const sourceHeight = 2600;
const wrapThresholdPx = 500;

function extractArray(source, name) {
  const match = source.match(new RegExp(`\\b${name}\\s*=\\s*(\\[[\\s\\S]*?\\])\\s*(?:\\n\\s*[A-Za-z_][A-Za-z0-9_]*\\s*=|\\n\\s*c\\s*=|\\n\\s*#)`, 'm'));
  if (!match) throw new Error(`Unable to find ${name} array.`);

  return JSON.parse(match[1].replaceAll("'", '"'));
}

function flushPath(paths, points) {
  if (points.length > 1) paths.push(points);
}

function buildBoundaries(constellations, xPoints, yPoints) {
  if (constellations.length !== xPoints.length || xPoints.length !== yPoints.length) {
    throw new Error(`Mismatched array lengths: const=${constellations.length}, x=${xPoints.length}, y=${yPoints.length}`);
  }

  const boundaries = [];
  let currentConstellation = constellations[0];
  let currentPaths = [];
  let currentPath = [[xPoints[0], yPoints[0]]];

  for (let index = 0; index < constellations.length - 1; index += 1) {
    const nextConstellation = constellations[index + 1];
    const start = [xPoints[index], yPoints[index]];
    const end = [xPoints[index + 1], yPoints[index + 1]];
    const sameConstellation = nextConstellation === currentConstellation;
    const connectsWithoutWrap = Math.abs(end[0] - start[0]) < wrapThresholdPx;

    if (sameConstellation && connectsWithoutWrap) {
      const last = currentPath.at(-1);
      if (!last || last[0] !== start[0] || last[1] !== start[1]) currentPath.push(start);
      currentPath.push(end);
      continue;
    }

    flushPath(currentPaths, currentPath);

    if (!sameConstellation) {
      boundaries.push({
        iau: currentConstellation,
        paths: currentPaths,
      });
      currentConstellation = nextConstellation;
      currentPaths = [];
    }

    currentPath = [end];
  }

  flushPath(currentPaths, currentPath);
  boundaries.push({
    iau: currentConstellation,
    paths: currentPaths,
  });

  return boundaries;
}

function pointBounds(xPoints, yPoints) {
  return {
    minX: Math.min(...xPoints),
    maxX: Math.max(...xPoints),
    minY: Math.min(...yPoints),
    maxY: Math.max(...yPoints),
  };
}

const source = await fs.readFile(inputPath, 'utf8');
const constellations = extractArray(source, 'const');
const xPoints = extractArray(source, 'boundryPointX');
const yPoints = extractArray(source, 'boundryPointY');
const boundaries = buildBoundaries(constellations, xPoints, yPoints);
const bounds = pointBounds(xPoints, yPoints);

const pointCount = boundaries.reduce((sum, boundary) => (
  sum + boundary.paths.reduce((pathSum, path) => pathSum + path.length, 0)
), 0);
const pathCount = boundaries.reduce((sum, boundary) => sum + boundary.paths.length, 0);

const output = [
  '// Generated by scripts/build-constellation-boundaries.mjs from app/oldConstellationBoundryScript.py.',
  'export const CONSTELLATION_BOUNDARIES = ',
  JSON.stringify({
    source: 'app/oldConstellationBoundryScript.py',
    sourceWidth,
    sourceHeight,
    plotBounds: bounds,
    wrapThresholdPx,
    count: boundaries.length,
    pathCount,
    pointCount,
    boundaries,
  }),
  ';',
  '',
].join('\n');

await fs.writeFile(outputPath, output);
console.log(`Wrote ${boundaries.length} constellation boundaries, ${pathCount} paths, and ${pointCount} points to ${outputPath.pathname}`);
